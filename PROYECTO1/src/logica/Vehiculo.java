package logica;

public class Vehiculo {
	String placa;
	String marca;
	String modelo;
	String color;
	String categoria;
	String estado;
	
	public Vehiculo(String placa, String marca, String modelo, String color, String categoria, String estado) {
		super();
		this.placa = placa;
		this.marca = marca;
		this.modelo = modelo;
		this.color = color;
		this.categoria = categoria;
		this.estado = estado;
	}
	
	
	
	
}
